import food.Food;
import mood.*;

public class Character {
    private int happinessPoints;

    public int getHappinessPoints() {
        return happinessPoints;
    }

    public void setHappinessPoints(int happinessPoints) {
        this.happinessPoints = happinessPoints;
    }

    public void eat(Food[] foods) {
        for (Food food : foods) {
            happinessPoints += food.getHappinessPoints();
        }
    }

    public Mood getCurrentMood(){
        if (happinessPoints < -5) {
            return new Angry();
        } else if (happinessPoints <= 0) {
            return new Sad();
        } else if (happinessPoints <= 15) {
            return new Happy();
        } else {
            return new VeryHappy();
        }
    }

    @Override
    public String toString() {
        Mood mood = getCurrentMood();
        return happinessPoints + " - " + mood.getClass().getSimpleName();
    }

}
